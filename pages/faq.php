    <h1>FAQ</h1>

    <h2>How does RegioSQM work?</h2>
    <p>RegioSQM finds the aromatic CH group with the highest proton affinity
    estimated by the PM3/COSMO method. A manuscript describing the method is in
    preparation.</p>

    <h2>Why do the calculations take so long?</h2>
    <p>The calculations are run on four CPUs on a CPU cluster shared with others.
    Your calculation wont start until CPUs become available.</p>

    <h2>Can I submit more than one molecule as once?</h2>
    <p>No. If you need to run a lot of molecules, please download the code (see next question) and run on your own computer.</p>

    <h2>Can I run RegioSQM on my own computer?</h2>
    <p>Yes, the source code is made freely available and hosted on GitHub.</p>


